int main() {
    stdio_init_all();

    gpio_init(PICO_DEFAULT_LED_PIN);
    gpio_set_dir(PICO_DEFAULT_LED_PIN, GPIO_OUT);
    gpio_put(PICO_DEFAULT_LED_PIN, 0);

    gpio_init(MODE_BUTTON);
    gpio_init(PLAY_BUTTON);
    gpio_init(NEXT_BUTTON);
    gpio_init(PREV_BUTTON);

    i2c_init(i2c_default, 400 * 1000);
    gpio_set_function(PICO_DEFAULT_I2C_SCL_PIN, GPIO_FUNC_I2C);
    gpio_set_function(PICO_DEFAULT_I2C_SDA_PIN, GPIO_FUNC_I2C);
    gpio_pull_up(PICO_DEFAULT_I2C_SCL_PIN);
    gpio_pull_up(PICO_DEFAULT_I2C_SDA_PIN);

    char morse_buf[5];
    char string_buf[20];

    init_display();

    init_sol(morse_buf, string_buf);
    init_ui();


    bool state, prev_state;
    absolute_time_t prev_switch_time;
    absolute_time_t current_time;

    bool is_high = 1;

    while (true) 
    {
        memset(buf + 1, 0x00, BUF_LEN - 1);
        draw_ui();

        current_time = get_absolute_time();

        prev_state = is_high;
        is_high = gpio_get(PLAY_BUTTON);

        gpio_get(MODE_BUTTON);

        gpio_put(PICO_DEFAULT_LED_PIN, is_high);

        if (!is_high && prev_state) 
        {
            absolute_time_t diff = absolute_time_diff_us(prev_switch_time, current_time);
            if (g_sol_state.morse_buf.len > 4 || diff < 10000)
                continue;

            if (diff < LONG_PRESS_THRESHOLD) 
                sol_string_push(&g_sol_state.morse_buf, '.');
            else
                sol_string_push(&g_sol_state.morse_buf, '-');

            prev_switch_time = current_time;
        };

        if (is_high && !prev_state) 
        {
            prev_switch_time = current_time;
        };

        if (!is_high && !prev_state) 
        {
            draw_outline_rect(g_ui_state.morse_hold_threshold, 0);

            if (g_sol_state.morse_buf.len == 0)
                continue;

            absolute_time_t diff = absolute_time_diff_us(prev_switch_time, current_time);
            if (diff > 1000000) 
            {
                printf("\t%s %c\n", morse_buf, decode_morse(morse_buf));
                char c = decode_morse(morse_buf);
                draw_text(20, 20, &c);
                sol_string_clear(&g_sol_state.morse_buf);
            };
        };

        if (is_high && prev_state) 
        {
            absolute_time_t diff = absolute_time_diff_us(prev_switch_time, current_time);
            unsigned long long fill_percent =  ((double)diff / LONG_PRESS_THRESHOLD) * g_ui_state.morse_hold_threshold.w;
            fill_percent = clamp(fill_percent, 0, g_ui_state.morse_hold_threshold.w);

            __draw_outline_rect(
                    g_ui_state.morse_hold_threshold.x, 
                    g_ui_state.morse_hold_threshold.y, 
                    fill_percent,
                    g_ui_state.morse_hold_threshold.h, 
                    1
                );
        };

        draw_text(2, 10, morse_buf);
        render_display();
        sleep_ms(10);
    };
};


void init_display() 
{
    uint8_t cmds[22] = 
    {
        0x00,           // control byte: <COMMAND>
        0xAE,           // set display off

        0xA8, 0x3F,     // set mux ratio to 63 + 1
        0xD3, 0x00,     // set display offset
        0x40,           // set display start line to 0
        0xA1,           // set segment remap to NO
        0xC8,           // set com direction 0 -> 63 (mux ratio)
        0xDA, 0x12,     // com pins hardware config (seq, no-remap)
        0x81, 0x7F,     // set contrast contral to 127
        0x20, 0x00,     // set mem_addressing_mode, horizontal addressing
        0xA4,           // entire display off, display whats on RAM
        0xA6,           // set !inverted display
        0xD5, 0xF0,     // set oscillator
        0x8D, 0x14,     // set charge pump

        0xAF,           // set display on
                        // 0xA5,           // entire display off, display whats on RAM
    };

    int res = i2c_write_blocking(i2c_default, 0x3C, cmds, 22, false);
    // if (res < 0) printf("no byte wrote!!!\n"); 
    // else printf("%i bytes wrote for init\n", res);

    for (int i = 1; i < BUF_LEN; ++i) 
        buf[i] = 0xFF;

    render_display();
    sleep_ms(200);

    for (int page = 0; page < 8; ++page) 
    {
        for (int i = 0; i < 129; ++i) 
        {
            int index = (page * 128 + i);
            if (index == 0)
                continue;
            buf[index] = 0x00;
        };
        render_display();

        sleep_ms(10);
    };

};

void render_display() 
{
    uint8_t draw_cmds[] = 
    {
        0x00,               // control byte: <COMMAND>
        0x21, 0x00, 0x7F,   // set col  address, 
        0x22, 0x00, 0x07,   // set page address, 
    };
    int res = i2c_write_blocking(i2c_default, 0x3C, draw_cmds, 7, false);
    // if (res < 0) printf("no byte wrote!!!\n"); 
    // else printf("%i bytes wrote for draw_commands\n", res);

    res = i2c_write_blocking(i2c_default, 0x3C, buf, 1025, false);
    // if (res < 0) printf("no byte wrote!!!\n"); 
    // else printf("%i bytes wrote for buffer\n", res);
};

