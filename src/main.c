static inline void draw_pixel(u8 x, u8 y, u8 color) {
    ++x;
    if (x < 0 || x >= 128 || y < 0 || y >= 64) return;

    size_t index = x + (y / 8) * 128;
    u8 bit   = y % 8;

    if (color) buf[index] |= (1 << bit);
    else buf[index] &= ~(1 << bit);
}

void __draw_outline_rect(u8 x, u8 y, u8 w, u8 h, u8 color) {
    for (u8 i = x; i < x + w; i++) {
        draw_pixel(i, y, color);
        draw_pixel(i, y + h - 1, color);
    }

    for (u8 j = y; j < y + h; j++) {
        draw_pixel(x, j, color);
        draw_pixel(x + w - 1, j, color);
    }
}

void __draw_filled_rect(u8 x, u8 y, u8 w, u8 h, u8 color) {
    for (u8 j = y; j < y + h; j++) {
        for (u8 i = x; i < x + w; i++) {
            draw_pixel(i, j, color);
        }
    }
}

void draw_filled_rect(Rect rect, u8 color) 
{
    __draw_filled_rect(rect.x, rect.y, rect.w, rect.h, color);
};

void draw_outline_rect(Rect rect, u8 color) 
{
    __draw_outline_rect(rect.x, rect.y, rect.w, rect.h, color);
};
static inline unsigned long long clamp(unsigned long long x, unsigned long long min, unsigned long long max) {
    if (x < min) return min;
    if (x > max) return max;
    return x;
}

void init_ui() 
{
    g_ui_state = (UIState) 
    {
        // TOP                          
        .morse_hold_threshold = (Rect) { .x = 0, .y = 0, .w = DISPLAY_WIDTH, .h = 2 },

        // BOTTOM RIGHT
        .sol_mode = (Rect) { .x = DISPLAY_WIDTH - 9, .y  = DISPLAY_HEIGHT - 9, .w = 8, .h = 8 },
    };
};

void draw_ui() 
{
    if (g_sol_state.mode == NORMAL)
        draw_char(g_ui_state.sol_mode.x, g_ui_state.sol_mode.y, 'N');
    else 
        draw_char(g_ui_state.sol_mode.x, g_ui_state.sol_mode.y, 'I');
};

void draw_char(u8 x, u8 y, char c) {
    int index;

    if (c == '.')
        index = 1;
    else if (c == '-')
        index = 2;
    else if (c >= 'A' && c <= 'Z')
        index = c - 'A' + 3;
    else if (c >= '0' && c <= '9')
        index = c - '0' + 29;
    else
        index = 0; // fallback (empty)

    uint8_t *glyph = &font[index * 8];

    for (int col = 0; col < 8; col++) {
        uint8_t line = glyph[col];

        for (int row = 0; row < 8; row++) {
            if (line & (1 << row)) {
                draw_pixel(x + col, y + row, 1);
            }
        }
    }
}

void draw_text(u8 x, u8 y, const char *str) {
    while (*str) {
        draw_char(x, y, *str++);
        x += 8; // 8 pixels per character
    }
}

