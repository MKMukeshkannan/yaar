int main() 
{
    /// {{{
    stdio_init_all();
    time_init();

    gpio_init(PICO_DEFAULT_LED_PIN);
    gpio_set_dir(PICO_DEFAULT_LED_PIN, GPIO_OUT);
    gpio_put(PICO_DEFAULT_LED_PIN, 1);

    gpio_init(MODE_BUTTON);
    gpio_init(PLAY_BUTTON);
    gpio_init(NEXT_BUTTON);
    gpio_init(PREV_BUTTON);


    // char x; while (1) { x = getchar(); if (x == '\n' || x == '\r') break; }
    // puts("Hello, world!");

    gpio_put(PICO_DEFAULT_LED_PIN, 0);
    sleep_ms(100);
    gpio_put(PICO_DEFAULT_LED_PIN, 1);
    // }}}

    init_display();

    FRESULT fr = f_mount(&sd_card.fatfs, sd_card.pcName, 1);
    if (fr != FR_OK)
        g_state.err = YAAR_MOUNT;


    ys_set(&g_state.location, "0:/");
    if (ls_dir()) 
        g_state.err = YAAR_DIR_OPEN_FAILED;

    // APPLICATION LOOOP
    g_state.rerender  = true;
    absolute_time_t current_time;
    while (true) 
    {
        current_time = get_absolute_time();
        fill_state_input(current_time);

        if (GET_MODE(g_state.btn_state) == 0b10) 
        {
            if (g_state.mode == NORMAL)
            {
                g_state.mode = INSERT;
            }
            else if (g_state.mode == INSERT)
            {
                ys_clear(&g_state.morse_buf);
                g_state.mode = VISUAL;
            }
            else if (g_state.mode == VISUAL) 
            {
                ys_clear(&g_state.morse_buf);
                g_state.mode = NORMAL;
            };

            g_state.rerender = true;
        };

        switch (g_state.mode) 
        {
            case NORMAL: { 
                             int cursor_mv_dir = 0;

                             if (GET_NEXT(g_state.btn_state) == 0b10) {
                                 cursor_mv_dir = 1;
                             } else if (GET_PREV(g_state.btn_state) == 0b10) {
                                 cursor_mv_dir = -1;
                             }

                             if (cursor_mv_dir != 0) {
                                 int new_cursor = g_state.ip_box_cursor + cursor_mv_dir;

                                 if (cursor_mv_dir < 0 && g_state.ip_box_cursor == 0)
                                     new_cursor = 0;

                                 g_state.ip_box_cursor = clamp(new_cursor, 0, g_state.ip_box_buf.len);
                                 g_state.rerender = true;
                             }

                             if (GET_PLAY(g_state.btn_state) == 0b10) // RELEASED
                             {
                                 search();
                                 g_state.rerender = true;
                             };

                             break; 
                         }
            case INSERT: 
                         { 
                             if (GET_PLAY(g_state.btn_state) == 0b10) // RELEASED
                             {
                                absolute_time_t diff = absolute_time_diff_us(g_state.playbt_prev_active, current_time);
                                if (diff > 10000) 
                                {
                                    if (diff < LONG_PRESS_THRESHOLD)
                                        ys_push(&g_state.morse_buf, '.');
                                    else
                                        ys_push(&g_state.morse_buf, '_');
                                };
                                g_state.threshold_meter = 0;
                                g_state.rerender = true;
                             };

                             if (GET_PLAY(g_state.btn_state) == 0b11)  // HELD
                             {
                                 absolute_time_t diff = absolute_time_diff_us(g_state.playbt_prev_active, current_time);
                                 unsigned long long fill_percent =  ((double)diff / LONG_PRESS_THRESHOLD) * DISPLAY_WIDTH;
                                 fill_percent = clamp(fill_percent, 0, DISPLAY_WIDTH);
                                 g_state.threshold_meter = fill_percent;
                                 g_state.rerender = true;
                             };

                             if (GET_NEXT(g_state.btn_state) == 0b10) // PRESSED STATE
                             {
                                 if (g_state.morse_buf.len != 0)
                                 {
                                    char c = decode_morse(g_state.morse_buf);
                                    if (c != '?') 
                                    {
                                        ys_insert(&g_state.ip_box_buf, c, g_state.ip_box_cursor);
                                        ys_clear(&g_state.morse_buf);
                                        g_state.rerender = true;
                                    };
                                 };
                             };

                             if (GET_PREV(g_state.btn_state) == 0b10) // PRESSED STATE
                             {
                                 if (g_state.morse_buf.len == 0) 
                                 {
                                     if (ys_delete(&g_state.ip_box_buf, g_state.ip_box_cursor)) 
                                         g_state.ip_box_cursor--;
                                 }
                                 else 
                                 {
                                     ys_clear(&g_state.morse_buf);
                                 }
                                 g_state.rerender = true;
                             };

                             break; 
                         };

            case VISUAL: { 
                             if (GET_NEXT(g_state.btn_state) == 0b10) // PRESSED STATE
                             {
                                 g_state.file_cursor = MIN(g_state.file_cursor + 1, g_state.total_ls);
                                 g_state.rerender = true;


                                 if (g_state.file_cursor > g_state.f_ls_range[1]) 
                                 {
                                     g_state.f_ls_range[0]++;
                                     g_state.f_ls_range[1]++;
                                 };

                             };

                             if (GET_PREV(g_state.btn_state) == 0b10) // PRESSED STATE
                             {
                                 if (g_state.file_cursor > 0) {
                                     g_state.file_cursor--;
                                     g_state.rerender = true;
                                 }

                                 if (g_state.file_cursor < g_state.f_ls_range[0]) 
                                 {
                                     g_state.f_ls_range[0]--;
                                     g_state.f_ls_range[1]--;
                                 };
                             };
                             break;
                         };
        }

        if (g_state.rerender) 
        {
            g_state.rerender = false;
            build_ui();
            render_display();
        };
        sleep_ms(10);
    };

    f_unmount(sd_card.pcName);
};

static inline unsigned long long clamp(unsigned long long x, unsigned long long min, unsigned long long max) 
{
    if (x < min) return min;
    if (x > max) return max;
    return x;
}

